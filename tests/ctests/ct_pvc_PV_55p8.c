bool test_pvc_PV_55p8_add(void)
{
    pvc_PV_55p8 a, b, res;
    bool s;
    test_start();

    // 1. 零 + 零
    a._1 = 0;
    b._1 = 0;
    s = pvc_PV_55p8_add(&a, &b, &res);
    assert_equal(res._1, 0);
    assert_equal(s, false);

    // 2. 正数相加，无溢出
    a._1 = 1;
    b._1 = 2;
    s = pvc_PV_55p8_add(&a, &b, &res);
    assert_equal(res._1, 3);
    assert_equal(s, false);

    // 3. 负数相加，无溢出
    a._1 = -1;
    b._1 = -2;
    s = pvc_PV_55p8_add(&a, &b, &res);
    assert_equal(res._1, -3);
    assert_equal(s, false);

    // 4. 正负相加，无溢出
    a._1 = 100;
    b._1 = -50;
    s = pvc_PV_55p8_add(&a, &b, &res);
    assert_equal(res._1, 50);
    assert_equal(s, false);

    // 5. 最大正数加0
    a._1 = INT64_MAX;
    b._1 = 0;
    s = pvc_PV_55p8_add(&a, &b, &res);
    assert_equal(res._1, INT64_MAX);
    assert_equal(s, false);

    // 6. 最小负数加0
    a._1 = INT64_MIN;
    b._1 = 0;
    s = pvc_PV_55p8_add(&a, &b, &res);
    assert_equal(res._1, INT64_MIN);
    assert_equal(s, false);

    // 7. 最大正数加1 → 正溢出
    a._1 = INT64_MAX;
    b._1 = 1;
    s = pvc_PV_55p8_add(&a, &b, &res);
    assert_equal(res._1, 0);
    assert_equal(s, true);

    // 8. 最小负数加 -1 → 负溢出
    a._1 = INT64_MIN;
    b._1 = -1;
    s = pvc_PV_55p8_add(&a, &b, &res);
    assert_equal(res._1, 0);
    assert_equal(s, true);

    // 9. 最大正数加最大正数 → 正溢出
    a._1 = INT64_MAX;
    b._1 = INT64_MAX;
    s = pvc_PV_55p8_add(&a, &b, &res);
    assert_equal(res._1, 0);
    assert_equal(s, true);

    // 10. 最小负数加最小负数 → 负溢出
    a._1 = INT64_MIN;
    b._1 = INT64_MIN;
    s = pvc_PV_55p8_add(&a, &b, &res);
    assert_equal(res._1, 0);
    assert_equal(s, true);

    // 11. 正数接近最大正数相加无溢出 (INT64_MAX-1) + 1 = INT64_MAX
    a._1 = INT64_MAX - 1;
    b._1 = 1;
    s = pvc_PV_55p8_add(&a, &b, &res);
    assert_equal(res._1, INT64_MAX);
    assert_equal(s, false);

    // 12. 负数接近最小负数相加无溢出 (INT64_MIN+1) + (-1) = INT64_MIN
    a._1 = INT64_MIN + 1;
    b._1 = -1;
    s = pvc_PV_55p8_add(&a, &b, &res);
    assert_equal(res._1, INT64_MIN);
    assert_equal(s, false);

    test_end();
}

